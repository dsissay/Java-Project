package com.MethodExercise.demo;

public class Calculation {
	static int a=89;
	static int b=56;
	
	public static void add()
	{
		
		System.out.println(a+b);
	}
	 static void sub()
	{
		System.out.println(a-b);
	}
	 static void mul()
	{
		System.out.println(a*b);
		
	}
	 static void div()
	{
		System.out.println(a/b);
		
	}

	public static void main (String[] args)
	{
		add();
		sub();
		mul();
		div();
		
	}

}
